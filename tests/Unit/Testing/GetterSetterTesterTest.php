<?php

namespace Aedart\Tests\Unit\Testing;

use Aedart\Tests\Helpers\Dummies\Traits\BoxTrait;
use Aedart\Tests\Helpers\Dummies\Traits\NameTrait;
use Aedart\Tests\TestCases\TraitTestCase;
use Codeception\Attribute\Group;
use PHPUnit\Framework\Attributes\Test;

/**
 * TraitTesterTest
 *
 * @author Alin Eugen Deac <aedart@gmail.com>
 * @package Aedart\Tests\Unit\Testing
 */
#[Group(
    'testing',
    'trait-tester',
    'getter-setter-tester',
)]
class GetterSetterTesterTest extends TraitTestCase
{
    #[Test]
    public function canAssertTraitMethods()
    {
        $this->assertGetterSetterTraitMethods(
            NameTrait::class,
            $this->faker->name(),
            $this->faker->name()
        );
    }

    #[Test]
    public function canAssertWithAutoGeneratedArgumentValue()
    {
        $this->assertTraitMethods(NameTrait::class);
    }

    #[Test]
    public function canAssertWithAutoGeneratedArgumentMock()
    {
        $this->assertTraitMethods(BoxTrait::class);
    }
}
